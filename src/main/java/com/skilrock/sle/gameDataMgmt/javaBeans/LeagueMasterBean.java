package com.skilrock.sle.gameDataMgmt.javaBeans;

public class LeagueMasterBean {
	
	private int leagueId;
	private String leagueType;
	private String leagueDevName;
	private String leagueDispName;
	private String status;
	public int getLeagueId() {
		return leagueId;
	}
	public void setLeagueId(int leagueId) {
		this.leagueId = leagueId;
	}
	public String getLeagueType() {
		return leagueType;
	}
	public void setLeagueType(String leagueType) {
		this.leagueType = leagueType;
	}
	public String getLeagueDevName() {
		return leagueDevName;
	}
	public void setLeagueDevName(String leagueDevName) {
		this.leagueDevName = leagueDevName;
	}
	public String getLeagueDispName() {
		return leagueDispName;
	}
	public void setLeagueDispName(String leagueDispName) {
		this.leagueDispName = leagueDispName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}
