package com.skilrock.sle.gameDataMgmt.javaBeans;

public class VenueMasterBean {
	
	private int venueId;
	private String venueDispName;
	private String venueCode;
	private String status;
	
	public int getVenueId() {
		return venueId;
	}
	public void setVenueId(int venueId) {
		this.venueId = venueId;
	}
	public String getVenueDispName() {
		return venueDispName;
	}
	public void setVenueDispName(String venueDispName) {
		this.venueDispName = venueDispName;
	}
	public String getVenueCode() {
		return venueCode;
	}
	public void setVenueCode(String venueCode) {
		this.venueCode = venueCode;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
